        <h2>Timeline</h2>
      </div>
      <div class="timeline-wrap">
        <div class="timeline-collection-list">
          <div class="timeline-item">
            <div class="timeline-event-wrap">
              <div class="timeline-item-no">BEGINNING</div>
              <div class="label cc-event-date">Jan 2014</div>
              <div class="timeline-event-name">Rohit and Kovid Start Holidify</div>
              <p class="paragraph">With the aim of making a travel discovery platform, We (<a href="https://www.linkedin.com/in/shroffrohit/?originalSubdomain=in">Rohit Shroff</a> and <a href="https://www.linkedin.com/in/kovid-kapoor-b9b9b814/">Kovid Kapoor</a>) started <a href="https://www.holidify.com/">Holidify</a> - a product that would help users find the best travel destinations across the world. They could choose and read in-depth information, choose from an exhaustive list of destinations according to their preferences, and plan their trip in entirety from scratch.</p>
              <div class="timeline-bottom-deco-line"></div>
              <div class="timeline-second-deco-line"></div>
            </div>
          </div>
          <div class="timeline-item">
            <div class="timeline-event-wrap">
              <div class="timeline-item-no">GROWTH</div>
              <div class="label cc-event-date">March 2015</div>
              <div class="timeline-event-name">Early Days and the Sweet Taste of First Success</div>
              <p class="paragraph">Holidify&#x27;s well-researched, in-depth, structured content starts getting early traction by users looking to plan their trips. Thereon, we focused on getting more accurate and in-depth travel content written for the users.</p>
              <div class="timeline-bottom-deco-line"></div>
              <div class="timeline-second-deco-line"></div>
            </div>
          </div>
          <div class="timeline-item">
            <div class="timeline-event-wrap">
              <div class="timeline-item-no">SURVIVAL</div>
              <div class="label cc-event-date">Jan 2016</div>
              <div class="timeline-event-name">Going Down, But Staying Afloat</div>
              <p class="paragraph">Unable to secure a follow-on round of funding from investors, and not having built a stable enough revenue stream, Holidify was forced to go into hibernation mode. We had to let the entire team go, and we ourselves took up jobs in parallel. But, we still remained committed to keep our product afloat and running, by investing time and money outside of working hours.</p>
              <div class="timeline-bottom-deco-line"></div>
              <div class="timeline-second-deco-line"></div>
            </div>
          </div>
          <div class="timeline-item">
            <div class="timeline-event-wrap">
              <div class="timeline-item-no">GROWTH</div>
              <div class="label cc-event-date">JAN 2018</div>
              <div class="timeline-event-name">Resurgent Growth</div>
              <p class="paragraph">Gradual and continuous efforts over the last two years led us to a position when we no longer needed funding to grow. With the aim of scaling our content offerings to the entire world, we embarked on a long-term goal of making a travel planning website for the entire world. We opened an office in Bangalore, built a team of motivated individuals, and start our mission. Slow and steady does win the race, it seems!</p>
              <div class="timeline-bottom-deco-line"></div>
              <div class="timeline-second-deco-line"></div>
            </div>
          </div>
          <div class="timeline-item">
            <div class="timeline-event-wrap">
              <div class="timeline-item-no">SURVIVAL</div>
              <div class="label cc-event-date">MAR 2020</div>
              <div class="timeline-event-name">COVID-19 and The Ongoing Survival</div>
              <p class="paragraph">With a traffic of 10M users in the month of Dec-2019, we made ambitious plans for 2020 - of staking a claim as the biggest travel planning website in Southeast Asia. But COVID-19 suddenly pulled down our revenues by 95%, and yet again, we had to go back into a survival mode. But we&#x27;ve already done it before, and we&#x27;re ready to do this again. Our motto-survive until you succeed!</p>
              <div class="timeline-bottom-deco-line"></div>
              <div class="timeline-second-deco-line"></div>
